class FFNet(nn.Module):
    def __init__(self, input_dim, output_dim, hidden_dim, norm_layer, use_dropout=False):
        super(FFNet, self).__init__()
        self.input_dim = input_dim
        self.output_dim = output_dim
        self.hidden_dim = hidden_dim
        current_dim = int(torch.prod(torch.tensor(input_dim)))

        self.layers = nn.ModuleList()
        for hdim in hidden_dim:
            self.layers.append(FFUnit(current_dim, hdim, norm_layer, use_dropout))
            current_dim = hdim
        if len(output_dim) > 1:
            self.layers.append(nn.Linear(current_dim, int(torch.prod(torch.tensor(output_dim)))))
        else:
            self.layers.append(nn.Linear(current_dim, output_dim[0]))
        

    def forward(self, x):
        if len(self.input_dim) > 1:
            x = x.view(x.size(0), -1)

        for layer in self.layers[:-1]:
            x = layer(x)
        out = self.layers[-1](x)
        if len(self.output_dim) > 1:
            out = out.view(out.size(0), *self.output_dim) # reshape to output_dim
        else:
            out = out.view(out.size(0), self.output_dim[0])
        return out


